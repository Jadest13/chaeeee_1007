export async function getStaticProps() {

  const res = await fetch('http://localhost:3000/api/db')
  const result = await res.json()

  if(!result) {
    return {
      redirect: {
        destination: '/',
        permanent: false,
      }
    }
  }

  return {
    props: {
      result,
    },

    revalidate: 10,
  }
}

